/*--------------------------------
Problem 8: Celsius to Fahrenheit Converter

Write a program that converts temperature from Celsius to Fahrenheit using the formula: F = (C × 9/5) +
32
--------------------------------*/

const celsiusVal = 28;

const fahrenheit = celsiusVal * (9 / 5) + 32;

console.log(`Celsius Value: ${celsiusVal}, Fahrenheit Value: ${fahrenheit}`);

/* OUTPUT:
Celsius Value: 28, Fahrenheit Value: 82.4
*/

/*--------------------------------
Problem 4: Sum and Average of Three Numbers

Write a program that takes three numbers as input and calculates their sum and average, then displays
both results
--------------------------------*/

let number1 = 53;
let number2 = 81;
let number3 = 28;

const sum = number1 + number2 + number3;

const average = sum / 3;

console.log(`Sum is: ${sum}, Average is: ${average}`);
// OUTPUT:  Sum is: 162, Average is: 54

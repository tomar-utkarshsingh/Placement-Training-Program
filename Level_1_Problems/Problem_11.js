/*--------------------------------
Problem 11: Area of Circle

Write a program that calculates the area of a circle by taking radius as input. Use formula: Area = π × r²
(use π = 3.14159)
--------------------------------*/

const PI = 3.14159;

const r = 15;

const areaOfCircle = PI * r ** 2;

console.log(`The r (radius) is: ${r}, then the`);
console.log(`Area of circle is: ${areaOfCircle}`);

/* OUTPUT:
The r (radius) is: 15, then the
Area of circle is: 706.85775
*/
